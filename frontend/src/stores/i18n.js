export const useI18nStore = defineStore('i18n', {
  state: () => ({
    idioma: localStorage.getItem('lakobra_idioma') || 'es'
  }),
  getters: {
    t: (state) => (clave) => {
      return traducciones[state.idioma]?.[clave] ?? traducciones['es'][clave] ?? clave
    }
  },
  actions: {
    cambiarIdioma(lang) {
      this.idioma = lang
      localStorage.setItem('lakobra_idioma', lang)
    }
  }
})
